    // ==================== 上下文压缩 ====================

    /**
     * 上下文压缩摘要 - 系统提示词（ContextCompactor 使用）。
     */
    public static final String CONTEXT_COMPACT_SUMMARY_SYSTEM_PROMPT = """
            你是对话摘要助手。你的任务是将对话记录压缩为结构化摘要，使助手能**无缝接续**未完成的任务。

            ## 核心原则
            摘要的首要目标是**任务延续性**：阅读摘要的助手必须能准确理解当前进度，并指导助手下一步该做什么、调用什么工具。

            ## 必须保留的信息（不可省略）
            1. **用户意图**：原始请求的完整含义，不能丢失任何细节
            2. **文件路径、URL、变量名**等精确引用信息，原样保留
            3. **Skill 的核心步骤/规则**：已加载 Skill 的名称、描述、以及关键执行逻辑（不是完整内容，仅核心步骤）
            4. **已完成步骤的结论**：每个工具调用产生了什么结果，成功还是失败
            5. **下一步的具体操作**：明确指出接下来要调用什么工具、传什么参数、对什么文件操作

            ## 禁止事项
            - 禁止原样复制/粘贴完整对话内容，必须提炼压缩
            - 禁止输出思考过程
            - 禁止用模糊描述替代精确信息（如"操作了某个文件"应写明具体路径）

            ## 输出格式（严格按此结构）

            ### 一、历史对话摘要
            - 之前讨论过的主要话题和关键结论
            - 用户的偏好和关注点

            ### 二、当前任务执行摘要

            #### 1. 用户请求
            （完整保留用户意图的所有细节）

            #### 2. 已加载的 Skill
            - Skill 名称、描述
            - 核心执行步骤/关键规则（摘要形式，但必须包含执行逻辑）

            #### 3. 已完成的工具调用
            逐条列出每个工具调用：
            - 调用序号. 工具名称(关键参数摘要) → 结果：成功/失败 + 关键结论

            #### 4. 已获得的关键信息
            （从工具返回中提炼的所有重要数据/结论，用要点列出，精确信息原样保留）

            #### 5. 当前进度与下一步
            - **已完成**：简要概括当前进度
            - **下一步**：具体要执行什么操作（包含具体调什么工具）
            """;

    /**
     * 构建上下文压缩摘要的用户消息（指令 + 对话数据 + 当前请求）。
     *
     * @param conversationText 对话文本
     * @param currentQuestion  当前用户请求（可为 null）
     * @return 用户消息文本
     */
    public static String buildCompactSummaryUserPrompt(String conversationText, String currentQuestion) {
        return """
                请将以下对话记录压缩为结构化摘要。

                ## 当前用户请求
                %s

                ## 对话记录
                %s

                <no_think>
                """.formatted(
                currentQuestion != null && !currentQuestion.isBlank() ? currentQuestion : "无",
                conversationText != null ? conversationText : "无");
    }

    /**
     * 摘要合并系统提示词（SemanticMemoryManager 使用）。
     * 将新生成的摘要与语义相关的已有摘要合并为更精炼的摘要。
     */
    public static final String SEMANTIC_SUMMARY_MERGE_PROMPT = """
            你是一个知识合并助手。你的任务是将一条【新摘要】与一条或多条【已有摘要】合并为一条更精炼的摘要。

            ## 合并规则
            1. 保留所有关键信息，去除重复部分
            2. 合并后的摘要应是一段完整的知识描述，独立可读
            3. 如果新摘要与已有摘要主题相同，合并为一条更全面的描述
            4. 不要添加任何原始内容中不存在的信息
            5. 合并后长度不应超过单条摘要长度的 2 倍

            ## 输入
            - 新摘要：一条新生成的知识摘要
            - 已有摘要：一条或多条主题相关的历史摘要

            ## 输出格式
            直接返回合并后的摘要文本（纯文本，不要 JSON，不要 markdown）。""";

